COUNT RES 1	
	ORG 0x00	
INIT:
	BCF STATUS,RP1	
	BSF STATUS,RP0	
	CLRF TRISB	
	MOVLW 0x06	
	MOVWF ADCON1	
	BCF STATUS,RP0	
	CLRF PORTB	
	;CLRF COUNT	
	CLRW		
CICLO:
	MOVLW 0x0F	
	ANDWF COUNT,0	
	CALL SEVENSEG_LUT
	MOVWF PORTB	
	INCF COUNT,1 	
	GOTO CICLO	
SEVENSEG_LUT:	
	ADDWF PCL,1	
	RETLW 0x3F
	RETLW 0x06
	RETLW 0x5B
	RETLW 0x4F
	RETLW 0x66
	RETLW 0x6D
	RETLW 0x7D
	RETLW 0x07
	RETLW 0x7F
	RETLW 0x6F
	RETLW 0x77
	RETLW 0x7C
	RETLW 0x39
	RETLW 0x5E
	RETLW 0x79
	RETLW 0x71